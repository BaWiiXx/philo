#include "../include/philo.h"

int	check_argument(char **av)
{
	int	i;

	i = 1;
	while (av[i])
	{
		ft_atoi(av[i]);
		if (ft_atoi(av[i]) == -1)
			return (1);
		++i;
	}
	return (0);
}
